"""TypeScriptAdapter — tree-sitter-typescript implementation of LanguageAdapter."""

from __future__ import annotations

import json
from pathlib import Path

import tree_sitter_typescript as tstypescript
from tree_sitter import Language, Node
from tree_sitter import Parser as TsParser

_TS_LANGUAGE = Language(tstypescript.language_typescript())
_TSX_LANGUAGE = Language(tstypescript.language_tsx())

# Queries that work for both TS and TSX grammars
_IMPORT_QUERY_TS = _TS_LANGUAGE.query(
    "(import_statement source: (string) @src)"
)
_IMPORT_QUERY_TSX = _TSX_LANGUAGE.query(
    "(import_statement source: (string) @src)"
)

_COMMENT_QUERY_TS = _TS_LANGUAGE.query("(comment) @c")
_COMMENT_QUERY_TSX = _TSX_LANGUAGE.query("(comment) @c")

# RHS node types that indicate a data-dominant initializer
_TS_DATA_LITERAL_TYPES: frozenset[str] = frozenset({"array", "object"})

# Auto-generation markers (same as Python adapter — language-agnostic)
_GENERIC_MARKERS: tuple[str, ...] = (
    "auto generated", "auto-generated", "autogenerated", "auto_generated",
    "automatically generated", "automatically-generated", "automatically created",
    "machine generated", "machine-generated", "machine created",
    "programmatically generated", "computer generated", "computer-generated",
    "generated by", "generated file", "generated code", "generated from",
    "generated source", "this file was generated", "this file is generated",
    "this file has been generated", "this file is auto-generated",
    "this file is autogenerated", "this file was automatically",
    "this file is automatically", "this is a generated file",
    "this file was created by", "this module is generated",
    "@generated", "@autogenerated", "@auto-generated", "@ generated",
)
_DO_NOT_EDIT_MARKERS: tuple[str, ...] = (
    "do not edit", "do not edit this file", "do not modify",
    "do not modify this file", "do not modify by hand",
    "do not change this file", "please do not edit", "please do not modify",
    "source of this file is",
)
_TOOL_MARKERS: tuple[str, ...] = (
    "generated by protoc", "generated by the protocol buffer compiler",
    "generated by swagger", "generated by openapi-generator", "generated by openapi",
    "generated by graphql", "generated by buf", "generated by betterproto",
    "created by transcrypt", "output of", "produced by", "compiled from",
    "transpiled from",
)
_ALL_MARKERS: tuple[str, ...] = _GENERIC_MARKERS + _DO_NOT_EDIT_MARKERS + _TOOL_MARKERS
_STRONG_FIRST_10_MARKERS: tuple[str, ...] = ("do not edit", "do not modify")


def _strip_quotes(text: str) -> str:
    """Strip surrounding quotes from a tree-sitter string node text."""
    if len(text) >= 2 and text[0] in ('"', "'", "`") and text[-1] == text[0]:
        return text[1:-1]
    return text


def _is_relative(specifier: str) -> bool:
    return specifier.startswith("./") or specifier.startswith("../")


def _extract_require_imports(root: Node) -> set[str]:
    """Walk AST for require('specifier') call expressions."""
    mods: set[str] = set()
    for node in _walk(root):
        if node.type != "call_expression":
            continue
        fn = node.child_by_field_name("function")
        args = node.child_by_field_name("arguments")
        if fn is None or args is None:
            continue
        if fn.type != "identifier" or (fn.text or b"").decode("utf-8") != "require":
            continue
        # First argument should be a string
        for arg in args.children:
            if arg.type == "string" and arg.text:
                spec = _strip_quotes(arg.text.decode("utf-8"))
                if spec and not _is_relative(spec):
                    mods.add(spec)
    return mods


def _walk(node: Node):  # type: ignore[return]
    """Depth-first traversal yielding every node."""
    yield node
    for child in node.children:
        yield from _walk(child)


def _get_ts_rhs(node: Node) -> Node | None:
    """Return the initializer child of a variable_declarator, or None."""
    found_eq = False
    for child in node.children:
        if child.type == "=" and not found_eq:
            found_eq = True
            continue
        if found_eq and child.is_named:
            return child
    return None


def _collect_ts_data_rows(root: Node, rows: set[int]) -> None:
    """Add row spans of top-level data-literal declarations to *rows*."""
    for child in root.children:
        target_decl: Node | None = None

        if child.type in ("lexical_declaration", "variable_declaration"):
            target_decl = child
        elif child.type == "export_statement":
            for sub in child.children:
                if sub.type in ("lexical_declaration", "variable_declaration"):
                    target_decl = sub
                    break

        if target_decl is None:
            continue

        for decl_child in target_decl.children:
            if decl_child.type != "variable_declarator":
                continue
            rhs = _get_ts_rhs(decl_child)
            if rhs is not None and rhs.type in _TS_DATA_LITERAL_TYPES:
                rows.update(range(decl_child.start_point[0], decl_child.end_point[0] + 1))


class TypeScriptAdapter:
    """LanguageAdapter implementation for TypeScript (.ts) and TSX (.tsx) source files."""

    file_extensions: frozenset[str] = frozenset({".ts", ".tsx"})

    def _get_parser_and_queries(
        self, extension: str
    ) -> tuple[TsParser, object, object]:
        if extension == ".tsx":
            parser = TsParser(_TSX_LANGUAGE)
            import_q = _IMPORT_QUERY_TSX
            comment_q = _COMMENT_QUERY_TSX
        else:
            parser = TsParser(_TS_LANGUAGE)
            import_q = _IMPORT_QUERY_TS
            comment_q = _COMMENT_QUERY_TS
        return parser, import_q, comment_q

    def extract_imports(self, source: str, extension: str = ".ts") -> set[str]:
        """Return non-relative module specifiers imported in *source*.

        Handles ``import ... from 'specifier'``, ``import 'specifier'`` (side-effect),
        and ``require('specifier')`` calls.  Relative imports are excluded.
        Returns empty set on parse error.
        """
        try:
            parser, import_q, _ = self._get_parser_and_queries(extension)
            tree = parser.parse(source.encode("utf-8"))
            captures: dict[str, list[Node]] = import_q.captures(tree.root_node)  # type: ignore[attr-defined]
            mods: set[str] = set()
            for node in captures.get("src", []):
                if node.text:
                    spec = _strip_quotes(node.text.decode("utf-8"))
                    if spec and not _is_relative(spec):
                        mods.add(spec)
            mods |= _extract_require_imports(tree.root_node)
            return mods
        except Exception:
            return set()

    def resolve_repo_modules(self, repo_root: Path) -> set[str]:
        """Return package names and tsconfig path alias prefixes for *repo_root*.

        Reads ``package.json`` (own name + workspace package names) and
        ``tsconfig.json`` ``compilerOptions.paths``.  Falls back gracefully if
        either file is absent or malformed.
        """
        mods: set[str] = set()

        pkg_json = repo_root / "package.json"
        if pkg_json.exists():
            try:
                data: dict[str, object] = json.loads(pkg_json.read_text(encoding="utf-8"))
                if name := data.get("name"):
                    mods.add(str(name))
                workspaces = data.get("workspaces", [])
                if isinstance(workspaces, dict):
                    workspaces = workspaces.get("packages", [])
                if isinstance(workspaces, list):
                    for pattern in workspaces:
                        if isinstance(pattern, str):
                            for ws_pkg in repo_root.glob(str(pattern) + "/package.json"):
                                try:
                                    ws_data: dict[str, object] = json.loads(
                                        ws_pkg.read_text(encoding="utf-8")
                                    )
                                    if ws_name := ws_data.get("name"):
                                        mods.add(str(ws_name))
                                except Exception:
                                    pass
            except Exception:
                pass

        tsconfig = repo_root / "tsconfig.json"
        if tsconfig.exists():
            try:
                tsdata: dict[str, object] = json.loads(tsconfig.read_text(encoding="utf-8"))
                compiler_opts = tsdata.get("compilerOptions", {})
                if isinstance(compiler_opts, dict):
                    paths = compiler_opts.get("paths", {})
                    if isinstance(paths, dict):
                        for alias in paths:
                            # "@scope/pkg/*" → "@scope/pkg"
                            prefix = str(alias).rstrip("/*").rstrip("/")
                            if prefix:
                                mods.add(prefix)
            except Exception:
                pass

        return mods

    def is_data_dominant(self, source: str, threshold: float = 0.65) -> bool:
        """Return True if the file is overwhelmingly composed of top-level data literals.

        Walks the tree-sitter AST (TS grammar) looking for top-level
        ``lexical_declaration`` / ``export_statement`` nodes where the
        ``variable_declarator`` value is an ``array`` or ``object`` literal.
        Computes ``data_literal_lines / total_nonblank_lines`` and compares
        against *threshold*.
        """
        if not source or not source.strip():
            return False
        try:
            parser = TsParser(_TS_LANGUAGE)
            tree = parser.parse(source.encode("utf-8"))
            root = tree.root_node
        except Exception:
            return False

        total_nonblank = sum(1 for line in source.splitlines() if line.strip())
        if total_nonblank == 0:
            return False

        data_rows: set[int] = set()
        _collect_ts_data_rows(root, data_rows)
        ratio = len(data_rows) / total_nonblank
        return ratio > threshold

    def is_auto_generated(self, source: str, head_lines: int = 40) -> bool:
        """Return True if the file header contains an auto-generation marker in a comment.

        Only inspects tree-sitter ``comment`` nodes (both ``//`` and ``/* */``).
        Falls back to a line-prefix heuristic if parsing fails.
        """
        if not source:
            return False
        try:
            parser = TsParser(_TS_LANGUAGE)
            tree = parser.parse(source.encode("utf-8"))
            captures: dict[str, list[Node]] = _COMMENT_QUERY_TS.captures(tree.root_node)
            comment_nodes: list[Node] = captures.get("c", [])

            for node in comment_nodes:
                line_index = node.start_point[0]
                if line_index >= head_lines:
                    continue
                text = node.text.decode("utf-8") if node.text else ""
                ll = text.lower()
                if line_index < 10 and any(m in ll for m in _STRONG_FIRST_10_MARKERS):
                    return True
                if any(m in ll for m in _ALL_MARKERS):
                    return True
            return False
        except Exception:
            lines = source.splitlines()
            head = lines[:head_lines]
            for line in head[:10]:
                stripped = line.lstrip()
                if not (stripped.startswith("//") or stripped.startswith("/*")):
                    continue
                ll = line.lower()
                if any(m in ll for m in _STRONG_FIRST_10_MARKERS):
                    return True
            for line in head:
                stripped = line.lstrip()
                if not (stripped.startswith("//") or stripped.startswith("/*")):
                    continue
                ll = line.lower()
                if any(m in ll for m in _ALL_MARKERS):
                    return True
            return False

    def prose_line_ranges(self, source: str, extension: str = ".ts") -> frozenset[int]:
        """Return 1-indexed line numbers covered by comments (line and block).

        TypeScript has no docstring concept; all prose is in comments.
        """
        try:
            parser, _, comment_q = self._get_parser_and_queries(extension)
            tree = parser.parse(source.encode("utf-8"))
            captures: dict[str, list[Node]] = comment_q.captures(tree.root_node)  # type: ignore[attr-defined]
            rows: set[int] = set()
            for node in captures.get("c", []):
                # Comment node spans start_point to end_point (0-indexed rows)
                start = node.start_point[0] + 1  # 1-indexed
                end = node.end_point[0] + 1
                rows.update(range(start, end + 1))
            return frozenset(rows)
        except Exception:
            return frozenset()
